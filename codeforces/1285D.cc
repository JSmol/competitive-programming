int f(vector<int> A, int n, int s) {
  if (s < 0) return 0;
  vector<int> a, b;
  for (int i = 0; i < n; i++) {
    if ((A[i] >> s) & 1) {
      a.pb(A[i]);
    } else {
      b.pb(A[i]);
    }
  }

  if (sz(a) > 0 and sz(b) > 0) {
    int ra = f(a, sz(a), s-1);
    int rb = f(b, sz(b), s-1);
    return min((1 << s) | ra, (1 << s) | rb);
  } else {
    return f((sz(a) > 0 ? a : b), n, s-1);
  }
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<int> A(n);
  for (auto& a : A) cin >> a;

  cout << f(A, n, 30) << endl;
}

