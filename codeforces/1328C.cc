int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; string s; cin >> n >> s;

    string a, b;
    for (int i = 0, p = 0; i < n; i++) {
      if (p) {
        if (s[i] == '0') {
          a += '0'; b += '0';
        }
        if (s[i] == '1') {
          a += '0'; b += '1';
        }
        if (s[i] == '2') {
          a += '0'; b += '2';
        }
      } else {
        if (s[i] == '0') {
          a += '0'; b += '0';
        }
        if (s[i] == '1') {
          a += '1'; b += '0';
          p = 1;
        }
        if (s[i] == '2') {
          a += '1'; b += '1';
        }
      }
    }

    cout << a << endl;
    cout << b << endl;
  }
}

