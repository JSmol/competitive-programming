typedef long long ll;
ll f(ll a, ll b, ll x, ll y, ll n) {
  ll k = min(n, a - x);
  n -= k;
  a -= k;

  ll l = min(n, b - y);
  n -= l;
  b -= l;

  return a*b;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int T; cin >> T;
  for (int C = 1; C <= T; C++) {
    ll a, b, x, y, n; cin >> a >> b >> x >> y >> n;
    cout << min(f(a,b,x,y,n), f(b,a,y,x,n)) << endl;
  }
}

