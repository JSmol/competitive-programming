double time() { return double(clock()) / CLOCKS_PER_SEC; }

int t;
long long n;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  cin >> t;
  while (t--) {
    cin >> n;

    int ans = 0;
    while (n % 5 == 0) {
      n = 4*(n/5);
      ans++;
    }

    while (n % 3 == 0) {
      n = 2*(n/3);
      ans++;
    }

    while (n % 2 == 0) {
      n = n / 2;
      ans++;
    }

    cout << (n == 1 ? ans : -1) << endl;
  }
}

