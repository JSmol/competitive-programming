int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    string s, x; cin >> s >> x;
    int n = sz(s);
    vector<vector<int>> A(n, vector<int>(256, -1));
    for (int l = 'a'; l <= 'z'; l++) {
      for (int i = n-1; i >= 0; i--) {
        if (s[i] == l) A[(n+i-1) % n][l] = i;
        else A[(n+i-1) % n][l] = A[i][l];
      }

      for (int i = n-1; i >= 0; i--) {
        if (A[i][l] == -1) A[i][l] = A[(i+1)%n][l];
      }
    }

    long long ans = 0;
    for (int i = 0, j = n-1; i < sz(x); i++) {
      if (A[j][x[i]] == -1) {
        ans = -1;
        break;
      }

      if (A[j][x[i]] <= j) ans++;
      j = A[j][x[i]];
    }

    cout << ans << endl;
  }
}

