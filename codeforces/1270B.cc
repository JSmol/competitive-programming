int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> A(n);
    for (auto& a : A) cin >> a;

    bool p = 0;
    for (int i = 0; i + 1 < n; i++) {
      if (abs(A[i] - A[i+1]) > 1) {
        cout << "YES" << endl;
        cout << i+1 << ' ' << i+2 << endl;
        p = 1;
        break;
      }
    }
 
    if (!p) cout << "NO" << endl;
  }
}

