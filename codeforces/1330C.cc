int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m; cin >> n >> m;
  vector<int> x(m);
  for (auto& a : x) cin >> a;
  vector<long long> X(m);
  for (int i = 0; i < m; i++)
    X[i] = (i ? X[i-1] : 0) + x[i];

  int p = n;
  vector<int> ans(m);
  for (int i = m-1; ~i; i--) {
    ans[i] = min(p, n-x[i]+1);
    while ((i ? X[i-1] : 0) < ans[i]-1) ans[i]--;
    p = ans[i] - 1;
  }

  if (p >= 0 and X[m-1] >= n) {
    for (int i = 0; i < m; i++)
      cout << ans[i] << " \n"[i == m-1];
  } else cout << -1 << endl;
}

