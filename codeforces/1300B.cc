int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> A(2*n);
    for (auto& a : A) cin >> a;
    sort(all(A));
    cout << A[(2*n)/2] - A[(2*n)/2-1] << endl;
  }
}

