int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, q; cin >> n >> q;
  vector<int> a(n);
  int s = 0;
  for (auto& x : a) cin >> x, s += x;
  for (int i = 0, t, x; i < q; i++) {
    cin >> t >> x;
    if (t == 1) {
      x--;
      a[x] ^= 1;
      if (a[x]) s++;
      else s--;
    } else {
      if (x <= s) cout << 1 << endl;
      else cout << 0 << endl;
    }
  }
}

