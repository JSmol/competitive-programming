int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> S(5000);
    for (int i = 0, x; i < n; i++) {
      cin >> x;
      if (x <= 2048) S[x]++;
    }

    for (int i = 1; i <= 1024; i *= 2) {
      while (S[i] >= 2) {
        S[2*i] += 1;
        S[i] -= 2;
      }
    }

    if (S[2048] > 0) cout << "YES" << endl;
    else cout << "NO" << endl;
  }
}

