int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<int> A(n);
  for (auto& a : A) cin >> a;

  for (int i = 30; i >= 0; i--) {
    int p = -1;
    for (int j = 0; j < n; j++) {
      if (p != -1 and (A[j] >> i) & 1) goto next;
      if ((A[j] >> i) & 1) p = j;
    }
    if (p != -1) { swap(A[0], A[p]); break; }
next:;
  }

  for (auto& a : A) cout << a << ' ';
  cout << endl;
}
