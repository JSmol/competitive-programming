int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    if (n > 1) {
      string s;
      if (n % 3 == 1) {
        for (int i = 0; i < n-2; i++)
          s += '2';
        s += "33";
        cout << s << endl;
      } else {
        for (int i = 0; i < n-1; i++) {
          s += '2';
        }
        s += '3';
        cout << s << endl;
      }
    } else cout << -1 << endl;
  }
}

