double time() { return double(clock()) / CLOCKS_PER_SEC; }

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  string s; cin >> s;
  int n = sz(s);
  vector<vector<long long>> B(n, vector<long long>(256));
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < 256; j++) {
      B[n-i-1][j] = (i ? B[n-i][j] : 0) + (s[n-i-1] == char(j));
    }
  }

  long long ans = 0;
  for (int i = 'a'; i <= 'z'; i++) {
    for (int j = 'a'; j <= 'z'; j++) {
      long long x = 0;
      for (int k = 0; k < n; k++) {
        if (s[k] == char(i))
          x += (k+1 < n ? B[k+1][j] : 0);
      }

      ans = max(x, ans);
    }
    ans = max(ans, B[0][i]);
  }

  cout << ans << endl;
}

