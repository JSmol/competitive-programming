int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int T; cin >> T;
  for (int C = 1; C <= T; C++) {
    int n, d;
    cin >> n >> d;
    vector<int> a(n);
    for (auto& x : a) cin >> x;
  
    int ans = 1;
    for (int i = 0; i < n; i++) {
      if (a[i] > d) ans = 0;
    }

    if (ans) { cout << "YES" << endl; continue; }

    for (int i = 0; i < n; i++)
      for (int j = i+1; j < n; j++) 
        if (a[i] + a[j] <= d) ans = 1;

    if (ans) { cout << "YES" << endl; continue; }
    else cout << "NO" << endl;
  }
}

