pair<int, int> ask(vector<int>& A) {
  cout << "? ";
  for (auto& a : A) cout << a << ' ';
  cout << endl;
  int x, y; cin >> x >> y;
  return {x, y};
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, k; cin >> n >> k;

  int b = 0, m = 0;
  vector<int> A;
  for (int i = 1; i <= k + 1; i++) {
    vector<int> X;
    for (int j = 1; j <= k + 1; j++) {
      if (i == j) continue;
      X.push_back(j);
    }

    auto [j, v] = ask(X);
    A.push_back(v);
    b = max(b, v);
  }

  m = 0;
  for (int i = 0; i <= k; i++) {
    if (A[i] == b) m++;
  }
  
  cout << "! " << m << endl;
}

