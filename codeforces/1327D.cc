int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> x(n);
    for (auto& a : x) cin >> a, a--;
    vector<int> c(n);
    for (auto& a : c) cin >> a, a--;

    vector<int> v(n);
    vector<vector<int>> a;
    for (int i = 0; i < n; i++) {
      if (!v[i]) {
        int j = i;
        vector<int> b;
        while (!v[j]) {
          b.push_back(j);
          v[j] = 1;
          j = x[j];
        }

        a.push_back(b);
      }
    }

    int ans = 1 << 30;
    for (auto& p : a) {
      int k = sz(p);
      for (int i = 1; i <= k; i++) {
        if (k % i == 0) {
          for (int l = 0; l < i; l++) {
            int f = 0;
            for (int j = l; j < k; j += i) {
              if (c[p[j]] != c[p[l]]) {
                f = 1;
                break;
              }
            }

            if (!f) {
              ans = min(ans, i);
              break;
            }
          }
        }
      }
    }

    cout << ans << endl;
  }
}

