const int inf = 1 << 30;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<int> a(n);
  for (auto& x : a) cin >> x;

  vector<vector<int>> dp(n, vector<int>(n));
  for (int r = 0; r < n; r++) {
    for (int l = r; ~l; l--) {
      if (l == r) dp[l][r] = a[l];
      else {
        for (int i = l; i < r; i++) {
          if (dp[l][i] > 0 and dp[l][i] == dp[i+1][r]) {
            dp[l][r] = dp[l][i] + 1;
          }
        }
      }
    }
  }

  vector<int> pd(n);
  for (int i = 0; i < n; i++) {
    pd[i] = (dp[0][i] ? 1 : inf);
    for (int j = 0; j < i; j++) {
      if (dp[j+1][i] > 0) pd[i] = min(pd[i], pd[j] + 1);
    }
  }

  cout << pd[n-1] << endl;
}

