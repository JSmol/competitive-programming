int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<vector<int>> a(n);
    for (auto& x : a) {
      int k; cin >> k;
      x.assign(k, 0);
      for (auto& y : x) cin >> y, y--;
    }

    set<int> S;
    int f = -1;
    for (int i = 0; i < n; i++) {

      bool p = 0;
      for (auto j : a[i]) {
        if (!S.count(j)) {
          S.insert(j);
          p = 1;
          break;
        }
      }

      if (!p) {
        f = i;
      }
    }

    if (f != -1) {
      cout << "IMPROVE" << endl;
      for (int i = 0; i < n; i++) {
        if (!S.count(i)) {
          cout << f+1 << ' ' << i+1 << endl;
          break;
        }
      }
    } else {
      cout << "OPTIMAL" << endl;
    }
  }
}

