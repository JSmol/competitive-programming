typedef long long ll;
ll inf = 1ll << 60;
ll f(vector<ll>& A, ll k) {
  ll ans = 0, e = 1;
  for (int i = 0; i < sz(A); i++) {
    if (ans > inf - abs(A[i] - e)) return inf;
    ans += abs(A[i] - e);
    if (e >= inf/k) return ans;
    e *= k;
  }

  return ans;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<ll> A(n); 
  for (auto& a : A) cin >> a;
  
  sort(all(A));
  if (n < 50) {
    ll ans = inf;
    for (int i = 1; i <= 1 << 20; i++) {
      ans = min(ans, f(A, i));
    }

    cout << ans << endl;
  } else {
    cout << accumulate(all(A), 0ll) - n << endl;
  }
}


