int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int T; cin >> T;
  for (int C = 1; C <= T; C++) {
    long long n; cin >> n;
    vector<long long> a(n);
    for (int i = 0; i < n; i++) {
      cin >> a[i];
    }

    long long ans = 0;
    vector<long long> x(n+1);
    for (int i = 0; i < n; i++) {
      x[i+1] += max(0ll, x[i] - (a[i] - 1));
      for (int j = i+2; j <= min({i+a[i], n}); j++)
        x[j] += 1;

      ans += max(0ll, a[i] - x[i] - 1);
      a[i] = 1;

    }

    cout << ans << endl;
  }
}

