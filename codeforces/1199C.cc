int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, k; cin >> n >> k;
  vector<int> A(n);
  for (auto& a : A) cin >> a;

  int x = 0;
  while (x < 30 and x*n <= 8*k) x++;
  x--;

  x = 1 << x;
  sort(all(A));
  vector<int> X(all(A));
  X.resize(distance(begin(X), unique(all(X))));
  if (x < sz(X)) {
    int ans = 1 << 30;
    for (int i = 0; i+x-1 < sz(X); i++) {
      int lb = lower_bound(all(A), X[i]) - begin(A);
      int ub = upper_bound(all(A), X[i+x-1]) - begin(A);
      // debug(X[i]);
      // debug(X[i+x-1]);
      // debug(lb);
      // debug(ub);
      ans = min(ans, (lb + (n - ub)));
    }
    cout << ans << endl;
  } else cout << 0 << endl;
}

