int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n, s, k; cin >> n >> s >> k;
    map<int, int> A;
    for (int i = 0, x; i < k; i++) {
      cin >> x; A[x] = 1;
    }
    
    int i = s, j = s, ans = 0;
    while (0 < j or i <= n) {
      if (i <= n and !A[i]) break;
      if (0 < j and !A[j]) break;
      ans++; i++; j--;
    }

    cout << ans << endl;
  }
}

