double time() { return double(clock()) / CLOCKS_PER_SEC; }

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<long long> A(n), X(n);
  for (auto& a : A) cin >> a;
  for (auto& a : X) cin >> a;

  vector<int> I(n);
  iota(all(I), 0);
  sort(all(I), [&] (int i, int j) { return A[i] == A[j] ? X[i] > X[j] : A[i] < A[j]; });

  long long ans = 0, s = 0;
  priority_queue<long long> Q;
  for (int i = 0, j = 0; i < n; i = j) {
    if (i > 0) {
      long long k = A[I[i-1]];
      while (!Q.empty() and k < A[I[i]]) {
        auto v = Q.top(); 
        s -= v; ans += s;
        Q.pop(); k++;
      }
    }

    j = i; while (j < n and A[I[i]] == A[I[j]]) j++;
    for (int k = i; k < j; k++) {
      s += X[I[k]];
      Q.push(X[I[k]]);
    }
  }

  while (!Q.empty()) s -= Q.top(), ans += s, Q.pop();
  cout << ans << endl;
}

