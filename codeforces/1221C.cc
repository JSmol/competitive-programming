int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int a, b, c; cin >> a >> b >> c;
    if (a < b) swap(a, b);

    int x = b;
    int ans = 0;
    if (c < x) {
      ans += c;
      a -= c;
      b -= c;
      x -= c;
      c = 0;
      ans += min((a+b)/3, b);
    } else {
      ans += b;
    }

    cout << ans << endl;
  }
}

