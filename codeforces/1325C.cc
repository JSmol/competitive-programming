int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;
  vector<int> D(n);
  vector<pair<int, int>> E(n-1);
  for (auto& [a, b] : E) {
    cin >> a >> b; a--; b--;
    D[a]++; D[b]++;
  }

  vector<int> ans(n-1);
  for (int i = 0, l = 0, r = n-2; i < n-1; i++) {
    if (D[E[i].fst] == 1 or D[E[i].snd] == 1) {
      ans[i] = l++;
    } else {
      ans[i] = r--;
    }
  }

  for (auto& a : ans) cout << a << endl;
}

