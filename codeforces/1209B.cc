const int m = 100000;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; string s; cin >> n >> s;
  vector<vector<int>> A(n, vector<int>(m, 0));
  for (int i = 0; i < n; i++) {
    A[i][0] = (s[i] == '1');
  }

  for (int i = 0; i < n; i++) {
    int a, b; cin >> a >> b;
    for (int j = 0; j < b; j++) {
      A[i][j] = A[i][0];
    }

    for (int j = 0, v = A[i][0]; b+j < m; j++) {
      if (j % a == 0) v ^= 1;
      A[i][b+j] = v;
    }
  }

  int ans = 0;
  for (int j = 0; j < m; j++) {
    int x = 0;
    for (int i = 0; i < n; i++) {
      x += A[i][j];
    }

    ans = max(ans, x);
  }

  cout << ans << endl;
}

