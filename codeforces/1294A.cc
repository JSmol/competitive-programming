int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; 
    vector<int> A(3);
    cin >> A[0] >> A[1] >> A[2] >> n;
    sort(all(A));
    if (n < (A[2] - A[0]) + (A[2] - A[1])) cout << "NO" << endl;
    else {
      int x = n - ((A[2] - A[0]) + (A[2] - A[1]));
      if (x % 3 == 0) cout << "YES" << endl;
      else cout << "NO" << endl;
    }
  }
}

