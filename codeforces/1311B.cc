int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n, m; cin >> n >> m;
    vector<int> A(n), X(n);
    for (auto& a : A) cin >> a;
    for (int i = 0, x; i < m; i++) cin >> x, X[x-1] = 1;

    for (int i = 0, j = 0; i < n; i = j) {
      j = i;
      while (j < n and X[j]) j++;
      if (i != j and j < n) sort(begin(A) + i, begin(A) + j + 1);
      else j++;
    }

    for (int i = 0; i+1 < n; i++) {
      if (A[i] > A[i+1]) goto no;
    }

    cout << "YES" << endl;
continue;
no: cout << "NO" << endl;
  }
}

