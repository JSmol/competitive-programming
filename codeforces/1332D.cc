int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int k; cin >> k;
  int s = 1;
  while (s-1-k <= 0) s <<= 1;
  s <<= 1;

  vector<vector<int>> a(3, vector<int>(4, 1));
  a[0][0] = a[2][2] = s-1;
  a[1][0] = a[2][0] = a[2][1] = a[2][3] = (s >> 1) - 1;
  a[0][1] = a[0][2] = a[1][2] = (s-1-k) | (s >> 1);

  cout << 3 << ' ' << 4 << endl;
  for (int i : {0,1,2}) {
    for (int j : {0,1,2,3})
      cout << a[i][j] << ' ';
    cout << '\n';
  }

}

