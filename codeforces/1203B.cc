int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> A(4*n);
    map<int, int> M; int mn = 1 << 30, mx = 0;
    for (int i = 0; i < 4*n; i++) {
      cin >> A[i];
      M[A[i]]++;
      mx = max(mx, A[i]);
      mn = min(mn, A[i]);
    }
  
    int p = 1;
    int v = mx * mn;
    for (auto [x, c] : M) {
      if (c > 0 and c % 2 == 0) {
        if (v % x) p = 0;
        else {
          if (M[v / x] != c) p = 0;
        }
      } else p = 0;
    }

    cout << (p ? "YES" : "NO") << endl;
  }
}

