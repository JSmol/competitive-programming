int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; string s; cin >> n >> s;
    vector<int> A;
    for (auto& a : s) A.push_back(a - '0');
  
    vector<int> B = A;
    sort(all(B));
    vector<int> ans(n);
    for (int i = 0, j = 0; i < n; i++) {
      if (A[i] == B[j]) {
        ans[i] = 1; j++;
      } else {
        ans[i] = 2;
      }
    }

    vector<int> C, D;
    for (int i = 0; i < n; i++)
      if (ans[i] == 1) C.push_back(A[i]);
    for (int i = 0; i < n; i++)
      if (ans[i] == 2) C.push_back(A[i]);

    D = C;
    sort(all(D));
    if (C == D) for (auto& a : ans) cout << a;
    else cout << '-';
    cout << endl;
  }
}

