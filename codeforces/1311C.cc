int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n, m; string s; cin >> n >> m >> s;
    vector<int> X(m);
    for (auto& x : X) cin >> x, x--;
    sort(all(X));
    vector<int> ans(256);
    for (int i = 0, j = 1; i < n; i++) {
      while (j <= m and X[m-j] == n-i-1) j++;
      ans[s[n-i-1]] += j;
    }

    for (int i = 'a'; i <= 'z'; i++) cout << ans[i] << " \n"[i == 'z'];
  }
}

