int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int T; cin >> T;
  for (int C = 1; C <= T; C++) {
    int n; cin >> n;
    vector<int> A(256);
    for (int i = 0; i < n; i++) {
      string s; cin >> s;
      for (int j = 0; j < sz(s); j++) {
        A[s[j]]++;
      }
    }

    for (int i = 0; i < 256; i++) {
      if (A[i] % n > 0) {
        cout << "NO" << endl;
        goto nxt;
      }
    }
    
    cout << "YES" << endl;
nxt:;
  }
}

