int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n, x; cin >> n >> x;
    set<int> S;
    for (int i = 0, a; i < n; i++) cin >> a, S.insert(a);

    int ans = 0;
    while (x > 0 or S.count(ans+1)) {
      if (S.count(ans+1)) ans++;
      else ans++, x--;
    }

    cout << ans << endl;
  }
}

