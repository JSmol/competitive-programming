int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m, p; cin >> n >> m >> p;
  vector<int> A(n), B(m);
  for (auto& a : A) cin >> a;
  for (auto& b : B) cin >> b;

  int ans = 0;
  for (int i = 0; i < n; i++) {
    if (A[i] % p) {
      ans += i;
      break;
    }
  }

  for (int i = 0; i < m; i++) {
    if (B[i] % p) {
      ans += i;
      break;
    }
  }

  cout << ans << endl;
}

