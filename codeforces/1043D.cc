int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m; cin >> n >> m;
  vector<vector<int>> a(m, vector<int>(n));
  for (auto& x : a) for (auto& y : x) cin >> y, y--;

  vector<int> p(n);
  for (int i = 0; i < n; i++) {
    p[a[0][i]] = i;
  }

  vector<vector<int>> b(m, vector<int>(n));
  for (int i = 0; i < m; i++) {
    for (int j = 0, k = 0; j < n; j += k) {
      int x = a[i][j];
      k = 0;
      while (j + k < n and p[x] + k < n and a[i][j + k] == a[0][p[x] + k]) k++;
      for (int l = 0; l < k; l++) {
        b[i][a[0][p[x] + l]] = k - l;
      }
    }
  }

  long long ans = 0;
  for (int j = 0; j < n; j++) {
    int x = 1 << 30;
    for (int i = 0; i < m; i++) {
      x = min(x, b[i][j]);
    }

    ans += x;
  }
  
  cout << ans << endl;
}

