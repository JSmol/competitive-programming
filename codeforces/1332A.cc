int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int a, b, c, d, x, y; cin >> a >> b >> c >> d >> x >> y;
    int xx = x - a + b;
    int yy = y - c + d;
    int xx1, yy1, xx2, yy2; cin >> xx1 >> yy1 >> xx2 >> yy2;
    
    if (xx1 == xx2 and (a != 0 or b != 0)) {
      cout << "No" << endl;
      continue;
    }

    if (yy1 == yy2 and (c != 0 or d != 0)) {
      cout << "No" << endl;
      continue;
    }

    if (xx1 <= xx and xx <= xx2
    and xx1 <= x  and x  <= xx2
    and yy1 <= yy and yy <= yy2
    and yy1 <= y  and y  <= yy2) cout << "Yes" << endl;
    else cout << "No" << endl;
  }
}

