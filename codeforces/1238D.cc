typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  ll n; string A; cin >> n >> A;
  ll ans = (n * (n - 1)) / 2;
  for (int i = 0, j = 0; i < n; i = j) {
    j = i + 1;
    while (j < n and A[i] == A[j]) j++;

    if (i-1 >= 0) {
      ans -= j - i - 1;
    }

    if (j < n) {
      ans -= j - i;
    }
  }

  cout << ans << endl;
}

