int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<int> A(n);
    for (auto& a : A) cin >> a;

    map<int, set<int>> X;
    for (int i = 0; i < n; i++) {
      int s = 0;
      while (A[i] % 2 == 0) {
        A[i] /= 2;
        s++;
      }
      X[A[i]].insert(-s);
    }

    int ans = 0;
    for (auto& x : X) {
      ans -= *begin(x.snd);
    }

    cout << ans << endl;
  }
}

