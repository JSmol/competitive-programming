int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    vector<vector<int>> X(1010);
    for (int i = 0, x, y; i < n; i++) {
      cin >> x >> y;
      X[x].push_back(y);
    }

    for (int i = 0; i < 1010; i++) {
      sort(all(X[i]));
    }

    bool p = 0;
    string ans;
    for (int i = 0, h = 0, col = 0; i < 1010; i++) {
      if (sz(X[i]) > 0) {
        if (X[i][0] < h) { p = 1;  break; }
        int b = X[i].back();
        while (h < b) {
          ans += "U"; 
          h++;
        }
        col += sz(X[i]);
        if (col == n) break;
      }

      ans += "R";
    }

    if (p) {
      cout << "NO" << endl;
    } else {
      cout << "YES" << endl;
      cout << ans << endl;
    }
  }
}

