int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, a, b, k; cin >> n >> a >> b >> k;
  vector<int> A(n);
  for (auto& x : A) cin >> x, x = (x % (a + b));

  int ans = 0;
  vector<int> X;
  for (int i = 0; i < n; i++) {
    if (A[i] == 0) A[i] += (a + b);
    if (A[i] <= a) ans++;
    else X.push_back(((A[i] + (a - 1)) / a) - 1);
  }
  
  sort(all(X));
  for (int i = 0; i < sz(X); i++) {
    if (k >= X[i]) {
      k -= X[i];
      ans++;
    }
  }

  cout << ans << endl;
}

