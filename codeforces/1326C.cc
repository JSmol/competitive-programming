typedef long long ll;
ll p = 998244353;

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, k; cin >> n >> k;
  vector<ll> A(n);
  for (auto& a : A) cin >> a;

  set<int> S;
  long long ans = 0;
  for (int i = 0; i < k; i++) {
    S.insert(n-i);
    ans += n-i;
  }

  long long ways = 1;
  int i = 0, j = 0;
  while (i < n and !S.count(A[i])) i++;
  for (; i < n; i = j) {
    j = i+1;
    while (j < n and !S.count(A[j])) j++;
    if (j != n) {
      ways *= ll(j - i);
      ways %= p;
    }
  }

  cout << ans << ' ' << ways << endl;
}

