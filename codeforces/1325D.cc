double time() { return double(clock()) / CLOCKS_PER_SEC; }

typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  ll x, s; cin >> x >> s;
  ll t = s - x;

  if (s == 0 and x == 0) {
    return cout << 0 << endl, 0;
  }

  if (t & 1 or t < 0) {
    return cout << -1 << endl, 0;
  }

  vector<ll> A;
  A.push_back(x);
  if (t > 0) {
    ll m = 0;
    for (int b = 62; ~b; b--) {
      if (t >> b & 1) {
        m |= 1ll << (b-1);
      }
    }

    A.push_back(m);
    for (int b = 62; ~b; b--) {
      if (!(A[0] >> b & 1)) {
        if (m >> b & 1) {
          m ^= 1ll << b;
          A[0] ^= 1ll << b;
        }
      }
    }

    if (m != 0) A.push_back(m);
  }

  cout << sz(A) << endl;
  for (auto& a : A) cout << a << ' ';
  cout << endl;
}

