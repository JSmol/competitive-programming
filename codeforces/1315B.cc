int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t; cin >> t;
  while (t--) {
    int a, b, p; string s; cin >> a >> b >> p >> s;
    int n = sz(s);
    vector<long long> X(n+1, 0);
    for (int i = n-2, j = n-2; i >= 0; i = j) {
      j = i; while (j >= 0 and s[i] == s[j]) j--;
      X[j+1] = X[i+1] + (s[i] == 'A' ? a : b);
    }

    for (int i = 0; i < n; i++) {
      if (X[i] != 0 and X[i] <= p) {
        cout << i+1 << endl;
        goto next;
      }
    }

    cout << n << endl;
next:;
  }
}

