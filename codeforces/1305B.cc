int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  string s; cin >> s; int n = sz(s);
  vector<vector<int>> X(2);
  for (int i = 0; i < n; i++) {
    X[s[i] == ')'].push_back(i);
  }

  vector<int> ans;
  int i = 0, j = sz(X[1])-1;
  while (i < sz(X[0]) and j >= 0) {
    if (X[0][i] < X[1][j]) {
      ans.push_back(X[0][i++]+1);
      ans.push_back(X[1][j--]+1);
    } else break;
  }

  sort(all(ans));
  if (sz(ans) > 0) {
    cout << 1 << endl;
    cout << sz(ans) << endl;
    for (auto& a : ans) cout << a << ' ';
    cout << endl;
  } else cout << 0 << endl;
}

