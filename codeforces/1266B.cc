typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int t;
  cin >> t;
  while (t--) {
    ll x;
    cin >> x;
    bool p = 0;
    for (ll c = 1; c <= 6; c++) {
      if (x - 7ll + c >= 14 and (x - 7ll + c) % 14ll == 0) p = 1;
    }

    cout << (p ? "YES" : "NO") << endl;
  }
}

