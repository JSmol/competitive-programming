int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, h; cin >> n >> h;
  vector<int> a(h);
  for (int i = 0; i < n; i++) {
    int x; cin >> x;
    if (i % 2) {
      a[h-x]++;
    } else {
      a[0]++;
      a[x]--;
    }
  }

  vector<int> x(h, 0);
  for (int i = 0, s = 0; i < h; i++) {
    s += a[i];
    x[i] = s;
  }

  int ans = *min_element(all(x));
  cout << ans << ' ' << count(all(x), ans) << endl;
}


