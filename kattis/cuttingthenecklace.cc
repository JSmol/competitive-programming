typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  ll k, n; cin >> k >> n; ll s = 0;
  vector<ll> A(n), X(n);
  for (int i = 0; i < n; i++) cin >> A[i], s += A[i], X[i] = s;
  if (s % k != 0) { cout << "NO" << endl; return 0; };
  ll d = s / k;
  map<ll, int> C;
  for (int i = 0; i < n; i++) X[i] %= d, C[X[i]]++;

  bool p = 0;
  for (auto& x : C)
    if (x.snd >= k) p = 1;

  cout << (p ? "YES" : "NO") << endl;
}

