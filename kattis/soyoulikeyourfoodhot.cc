typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  
  int x, y;
  char dot;

  cin >> x >> dot >> y;
  ll a = x * 100 + y;

  cin >> x >> dot >> y;
  ll b = x * 100 + y;

  cin >> x >> dot >> y;
  ll c = x * 100 + y;

  ll some = 0;
  for (ll sd = 0; a - sd*b >= 0; sd++) {
    if ((a - sd*b) % c == 0) {
      ll td = (a - sd*b) / c;
      assert(td >= 0 and sd >= 0);
      cout << sd << ' ' << td << '\n';
      some = 1;
    }
  }

  if (!some) cout << "none" << endl;
}

