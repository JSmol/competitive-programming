typedef long long ll;
ll inf = 1ll << 58;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, a, b; cin >> n >> a >> b;
  if (a > b) swap(a, b);
  vector<int> A(n);
  for (auto& a : A) cin >> a;
  vector<ll> X(n);
  for (int i = 0; i < n; i++) X[i] = (i ? X[i-1] : 0) + A[i];

  vector<vector<ll>> dp(n, vector<ll>(a+1, -1ll));
  function<ll(int, int)> f = [&] (int i, int s) {
    if (i == n) return 0ll;
    ll& x = dp[i][s];
    if (x != -1) return x;
    if (s + A[i] <= a) x = max(x, f(i+1, s+A[i]) + 1);
    if ((i ? X[i-1] : 0) - s + A[i] <= b) x = max(x, f(i+1, s) + 1);
    return x = max(x, 0ll);
  };

  cout << f(0, 0) << endl;
}

