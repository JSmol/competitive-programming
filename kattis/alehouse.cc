int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, k; cin >> n >> k;
  vector<pair<int, int>> X(n);
  for (auto& x : X) cin >> x.snd >> x.fst;

  sort(all(X));
  vector<int> a(n), b(n);
  vector<pair<int, int>> x(n), y(n);
  for (int i = 0; i < n; i++) {
    b[i] = X[i].fst;
    a[i] = X[i].snd;
    x[i] = { a[i], i };
    y[i] = { b[i], i };
  }

  sort(all(x)); sort(all(y));

  int ans = 0;
  // debug(a); debug(b);
  // debug(x); debug(y);

  set<int> S;
  for (int i = 0, j = 0, l = 0; i < n; i++) {
    while (j < n and x[j].fst <= b[i] + k) {
      S.insert(x[j].snd);
      j++;
    }

    while (l < n and y[l].fst < b[i]) {
      S.erase(y[l].snd);
      l++;
    }

    ans = max(ans, sz(S));
    // debug(mp(b[i], S));
  }

  cout << ans << endl;
}

