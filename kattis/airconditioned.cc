int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m = 0; cin >> n;
  vector<int> L(n), R(n);
  for (int i = 0; i < n; i++) {
    cin >> L[i] >> R[i]; m = max({m, L[i], R[i]});
  }

  vector<set<int>> A(m+2), B(m+2);
  for (int i = 0; i < n; i++) {
    A[L[i]].insert(i);
    B[R[i]].insert(i);
  }

  int ans = 0;
  set<int> S, C;
  for (int i = 1; i <= m; i++) {
    for (auto& a : A[i]) S.insert(a);

    bool p = 0;
    for (auto& b : B[i]) {
      if (!C.count(b)) {
        p = 1;
      }
    }

    if (p) {
      for (auto& s : S) {
        C.insert(s);
      }

      S.clear();
      ans++;
    }

  }

  cout << ans << endl;
}

