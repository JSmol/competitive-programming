int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; char _;
  while (cin >> n >> _ && n) {
    vector<int> A(n);
    for (auto& a : A) cin >> a;
    vector<int> X(n), Y(n, 1);
    for (auto& i : A) {
      X[i] = 1; Y[i] = 0;
      for (int j = -n; j <= n; j++) {
        if (0 <= i - j and i - j < n and 0 <= i + j and i + j < n) {
          if (X[i-j] and Y[i+j]) {
            cout << "no" << endl;
            goto nxt;
          }
        }
      }
    }
  
    cout << "yes" << endl;
nxt:;
  }
}

