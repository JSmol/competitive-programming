int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  vector<string> A = {"A", "A#", "B", "C", "C#", "D", "D#", "E", "F", "F#", "G", "G#"};
  map<string, set<string>> M;
  for (int i = 0; i < 12; i++) {
    for (int j : {0,2,4,5,7,9,11}) {
      M[A[i]].insert(A[(i+j) % 12]);
    }
  }

  set<string> S;
  for (auto& a : A) S.insert(a);

  int n; cin >> n;
  for (int i = 0; i < n; i++) {
    string x; cin >> x;
    for (auto& [c, y] : M) {
      if (!y.count(x)) {
        S.erase(c);
      }
    }
  }

  if (!S.empty()) {
    for (auto& s : S) cout << s << ' ';
    cout << endl;
  } else {
    cout << "none" << endl;
  }
}

