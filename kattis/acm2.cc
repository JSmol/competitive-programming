int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, p; cin >> n >> p;
  vector<int> A(n);
  for (auto& a : A) cin >> a;
  swap(A[0], A[p]);
  sort(begin(A)+1, end(A));
  int ans1 = 0, ans2 = 0;
  int t = 0, i = 0;
  while (i < n) {
    if (t + A[i] <= 300) {
      t += A[i]; i++;
      ans1++;
      ans2 += t;
    } else {
      break;
    }
  }

  cout << ans1 << ' ' << ans2 << endl;
}

