typedef long long ll;
template<typename T>
T bpow(T x, ll n) { return n ? n % 2 ? x * bpow(x, n - 1) : bpow(x * x, n / 2) : T(1); }

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int c, n, m; cin >> c >> n >> m;
  vector<int> a(n);
  for (auto& x : a) cin >> x;
  vector<int> d(m);
  for (auto& x : d) cin >> x;

  vector<int> x(n);
  for (int i = 0; i < n; i++) {
    while (a[i] * bpow(2ll, x[i]) <= c) x[i]++;
    x[i]--;
  }

  for (auto& e : d) {
    ll ans = 0;
    for (int i = 0; i < n; i++) {
      ans += bpow(2ll, max(0, e - x[i]));
    }

    cout << ans << endl;
  }
}

