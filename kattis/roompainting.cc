int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, m; cin >> n >> m;
  vector<long long> a(n), b(m);
  for (auto& x : a) cin >> x;
  for (auto& x : b) cin >> x;


  sort(all(a));

  long long ans = 0;
  for (auto& x : b) {
    auto ub = upper_bound(all(a), x);
    assert(ub != end(a));

    if (ub != begin(a) and *(ub-1) == x) continue;

    ans += (*ub) - x;
  }


  cout << ans << endl;
}

