double time() { return double(clock()) / CLOCKS_PER_SEC; }

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n, s, k; cin >> n >> s >> k;

  vector<vector<double>> dp(n+1, vector<double>(s+1));
  dp[0][0] = 1;
  for (int i = 1; i <= n; i++) {
    for (int j = 1; j <= s; j++) {
      dp[i][j] += double((s - j + 1)*1. / s) * dp[i-1][j-1]
                + double(j*1. / s) * dp[i-1][j];
    }
  }

  double ans = 0.;
  for (int i = k; i <= s; i++) 
    ans += dp[n][i];
    
  cout << fixed << setprecision(9) << ans << endl;
}

