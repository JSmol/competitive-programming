int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n; cin >> n;  
  vector<int> a(n);
  for (auto& x : a) cin >> x;

  vector<int> ans = { a[0] };
  for (int i = 1; i < n; i++) {
    if (ans.back() < a[i]) {
      ans.push_back(a[i]);
    }
  }

  cout << sz(ans) << endl;
  for (int x : ans) cout << x << ' ';
  cout << endl;
}

