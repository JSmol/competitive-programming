int n, m, a, b, c, d, e, f, v;
int dp[222][222][222];
int g(int i, int j, int p) {
  if (i == m) return 0;

  int& x = dp[i][j][p];
  if (x != -1) return x;

  int vi = ((a * (i+1) + b * p) % c) + 1;
  int li = (d * (i+1) + e * p) % f;
  x = 0;

  if (j + vi <= n) x = max(x, g(i+1, j+vi, 0) + li);
  x = max(x, g(i+1, j, vi));

  return x;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  cin >> n >> m >> a >> b >> c >> d >> e >> f >> v;
  for (int i = 0; i < 222; i++)
    for (int j = 0; j < 222; j++)
      for (int k = 0; k < 222; k++)
        dp[i][j][k] = -1;
  cout << g(0, 0, v) << endl;
}

