int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int N = 3e6;
  vector<int> p(N, 1); p[0] = p[1] = 0;
  for (int i = 2; i < N; i++) {
    if (p[i]) {
      for (int j = i+i; j < N; j += i) {
        p[j] = 0;
      }
    }
  }

  vector<int> ans(N, 0);
  for (int i = 1; i < N; i++) {
    if (!p[i]) {
      for (int j = i; j < N; j += i) {
        ans[j]++;
      }
    }
  }

  int n; cin >> n;
  vector<int> in(n);
  for (int i = 0; i < n; i++) {
    cin >> in[i];
  }

  for (int i = 0; i < n; i++)
    cout << ans[in[i]] << '\n';
}

