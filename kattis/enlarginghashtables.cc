typedef long long ll;
int main() {
  ios::sync_with_stdio(0); cin.tie(0);

  ll N = 1 << 18;
  vector<int> p(N, 1);
  vector<ll>primes;
  for (ll i = 2; i < N; i++) {
    if (p[i]) {
      primes.push_back(i);
      for (ll j = i*i; j < N; j += i) {
        p[j] = 0;
      }
    }
  }


  ll n;
  while (cin >> n and n != 0) {
    ll x = n;
    int q = 1;
    for (auto& p : primes) {
      if (x > p and x % p == 0) {
        q = 0;
        break;
      }
    }

    x = 2*x;
    while (x++) {
      int r = 1;
      for (auto& p : primes) {
        if (x > p and x % p == 0) {
          r = 0;
          break;
        }
      }

      if (r) break;
    }

    cout << x << (!q ? " (" + to_string(n) + " is not prime)\n" : "\n");
  }
}


