int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int n = 5000;
  vector<vector<int>> dp(n, vector<int>(n));
  // i trys for range [1..j]
  dp[0][0] = dp[0][1] = 1;
  for (int i = 1; i < n; i++) {
    for (int j = 0; j < n; j++) {
      if (j < i) dp[i][j] = 1;
      else dp[i][j] = dp[i-1][j-i] | dp[i-1][j];
    }
  }

  while (cin >> n && n) {
    for (int i = 0; i <= n; i++) {
      if (dp[i][n]) {
        cout << i << endl;
        break;
      }
    }
  }
}

