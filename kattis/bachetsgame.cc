int n, m;
int a[20];
int dp[1010101];
int f(int s) {
  int& x = dp[s];
  if (x != -1) return x;
  if (s == 0) return 0;
  for (int i = 0; i < m; i++) {
    if (s - a[i] >= 0) {
      if (!f(s-a[i])) {
        return x = 1;
      }
    }
  }

  return x = 0;
}

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  while (cin >> n >> m) {
    for (int i = 0; i < m; i++) cin >> a[i];
    for (int i = 0; i <= n; i++)
      dp[i] = -1;
    if (f(n) == 1) cout << "Stan wins" << endl;
    else cout << "Ollie wins" << endl;
  }
}

